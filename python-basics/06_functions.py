# --- 関数の定義 ---
# def 関数名(引数1, 引数2, ...):
#     処理
#     return 戻り値
def add(a, b):
    """2つの数値を受け取り、その合計を返す関数"""
    result = a + b
    return result

# --- 関数の呼び出し ---
sum_result = add(5, 3)
print("5 + 3 =", sum_result)


