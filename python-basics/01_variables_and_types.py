# --- 1. 変数とデータ型 ---
# Pythonでは、値に名前を付けて保存することができます。これを「変数」と呼びます。

# --- 文字列 (string) ---
# ' や " で囲んだテキストデータ
message = "こんにちは、Python！"
print(message)

# --- 整数 (integer) ---
# 小数点のない数値
width = 1920
height = 1080
print("画像の幅:", width)
print("画像の高さ:", height)

# --- 浮動小数点数 (float) ---
# 小数点のある数値
pi = 3.14159
print("円周率:", pi)

# --- 真偽値 (boolean) ---
# True (真) または False (偽) のどちらかの値
is_processed = False
print("処理済みフラグ:", is_processed)


