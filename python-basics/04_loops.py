# --- 4. ループ (for) ---
# リストなどのコレクションに含まれる各要素に対して、同じ処理を順番に実行します。
# 画像の全ピクセルを処理したり、複数のファイルに対して同じ操作をしたりする際に必須です。

# --- forループの基本 ---
colors = ["red", "green", "blue"]

print("--- 色を一つずつ表示 ---")
for color in colors:
    print(color)

# --- range()を使った繰り返し ---
# 指定した回数だけ処理を繰り返したい場合に使います。
# range(5) は 0, 1, 2, 3, 4 という数値のシーケンスを生成します。
print("--- 5回繰り返す ---")
for i in range(5):
    print(f"{i+1}回目の繰り返し") # f-string: 文字列の中に変数を埋め込める


