# --- 5. 条件分岐 (if) ---
# 条件が満たされているかどうかに応じて、処理の流れを変えることができます。
# 「もし○○ならばAを実行し、そうでなければBを実行する」といった制御を行います。

# --- if-elseの基本 ---
temperature = 30

if temperature >= 25:
    print("暑いですね！")
else:
    print("過ごしやすいですね。")

# --- if-elif-else ---
# 複数の条件で分岐させることもできます。
score = 85

if score >= 90:
    print("優")
elif score >= 80:
    print("良")
elif score >= 60:
    print("可")
else:
    print("不可")


